#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char c;

	if (s == NULL)
		return ;
	while (*s != '\0')
	{
		c = *s;
		write(fd, &c, 1);
		s++;
	}
	write(fd, "\n", 1);
}
