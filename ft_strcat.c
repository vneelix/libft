#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *b;

	b = s1;
	while (*s1 != '\0')
		s1++;
	while (*s2 != '\0')
	{
		*s1 = *((char*)s2);
		s1++;
		s2++;
	}
	*s1 = '\0';
	return (b);
}
