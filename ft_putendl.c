#include "libft.h"

void	ft_putendl(char const *s)
{
	char c;

	if (s == NULL)
		return ;
	while (*s != '\0')
	{
		c = *s;
		write(1, &c, 1);
		s++;
	}
	write(1, "\n", 1);
}
